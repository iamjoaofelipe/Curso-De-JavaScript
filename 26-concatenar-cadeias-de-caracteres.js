// Concatenar cadeias de caracteres em JavaScript.

var nomeCompleto = "João" + "Felipe";
// Sem espaço
console.log(nomeCompleto);

var nomeCompleto = "João " + "Felipe";
// Com espaço
console.log(nomeCompleto);

var nomeCompleto = "João" + " Felipe";
// Com espaço
console.log(nomeCompleto);

var nomeCompleto = "João" + " " +"Felipe"; 
// Com espaço
console.log(nomeCompleto);

