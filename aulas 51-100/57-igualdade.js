// Operador de igualdade em JavaScript.
console.log(5 == 5); // true.
console.log(6 == 5); // false
console.log("Olá" == "Olá"); // true.
console.log("Olá" == "JavaScript"); // false.
console.log("Olá" == "olá"); // false
console.log([1,2,3] == [1,2,3]); // false. Não pode comparar dois objetos na memória.
console.log([1,2,3] == [4,5,6]); // false.


