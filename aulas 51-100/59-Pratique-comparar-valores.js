// Operador de igualdade e estrita JavaScript.
var a;
var b;
a = 5;
b = 5;
console.log(a == b) // true.
console.log(a === b); // true.
b = 8; // valor alterado.
console.log(a == b); // false.
console.log(a === b); // false.
b = "5"; // valor alterado.
console.log(a == b); // true.
console.log(a === b) // false
a = "JavaScript"; // valor alterado.
b = "Javascript"; // valor alterado.
