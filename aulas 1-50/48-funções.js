// Funções , argumentos, concatenação em JavaScript.
function somar(a, b) {
    soma = a + b;
    console.log("O resultado de " + a + " + " + b + " é: " + soma);
}
somar(5, 3);
somar(8, 9);
somar(2, 4);
somar(5, 5);
// exemplo 1.
var x = 5;
var y = 3;
somar(x, y);
