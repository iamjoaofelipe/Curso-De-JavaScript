// Alguns tipos de dados em JavaScript.
/*
undefined, null, boolean, string, symbol, number, object..
undefined representa um valor indefinido.
null é um literal em JavaScript que representa um valor nulo ou "vázio".
O objeto Boolean é um objeto wrapper para um valor booleano. verdadeiro ou falso.
O objeto global "String"  é um construtor para strings, ou uma sequência de "caracteres".
A função  Symbol() retorna um valor do tipo símbolo (símbolo)
O objeto JavaScript Number é um objeto encapsulado que permite você trabalhar com valores numéricos.
Um objeto Number é criado utilizando o construtor Number().
Objeto é uma coleção de propriedades, e uma propriedade é uma associação entre um nome (ou chave) e um valor.
Um valor de propriedade pode ser uma função, que é então considerada um método do objeto.
*/
