// Acessando as informações das arrays em javaScript.
var meuArray = [[1,2,3] , [4,5,6] , [7,8,9]];
/*
[[1, 2, 3], [4, 5, 6], [7, 8, 9]]
0          1          2
0  1  2    0  1  2    0  1  2
*/
console.log(meuArray[0]); // [1,2,3] 
console.log(meuArray[1]); // [4,5,6]
console.log(meuArray[2]); // [7,8,9]
// acessando as informações do primeiro array.
console.log(meuArray[0][0]);
console.log(meuArray[0][1]);
console.log(meuArray[0][2]);
// acessando as informações da segunda array.
console.log(meuArray[1][0]);
console.log(meuArray[1][1]);
console.log(meuArray[1][2]);
// acessando as informações da terceira array.
console.log(meuArray[2][0]);
console.log(meuArray[2][1]);
console.log(meuArray[2][2]);


